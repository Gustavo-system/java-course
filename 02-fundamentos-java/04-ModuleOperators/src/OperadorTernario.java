public class OperadorTernario {
    public static void main(String[] args) {
        // Operador ternario
        // siempre retorna un valor
        // puede retornar cualquier tipo de dato

        //variable = condicion ? verdadero : falso;
        String isTrue = 7 > 5 ? "el 7 es mayor que el 5" : "el 5 es menor que el 7";
        System.out.println("Ternario: " + isTrue);

    }
}
